        /// <summary>
        /// Generates a valid JWT token with claims matching the monolith's
        /// <c>AuthService.BuildTokenAsync</c> method (lines 145-160) EXACTLY:
        /// <list type="bullet">
        ///   <item><c>ClaimTypes.NameIdentifier</c> — user ID (line 148)</item>
        ///   <item><c>ClaimTypes.Email</c> — email address (line 149)</item>
        ///   <item><c>ClaimTypes.Role.ToString()</c> — one per role (line 150)</item>
        ///   <item><c>"token_refresh_after"</c> — binary DateTime (lines 152-153)</item>
        /// </list>
        /// Algorithm: <c>SecurityAlgorithms.HmacSha256Signature</c> (line 156).
        /// Expiry: 1440 minutes / 24 hours (line 19).
        /// </summary>
        /// <param name="userId">User ID for the NameIdentifier claim.</param>
        /// <param name="email">Email address for the Email claim.</param>
        /// <param name="roles">Role names for Role claims.</param>
        /// <returns>A JWT token string in compact serialization format.</returns>
        public string GenerateJwtToken(Guid userId, string email, string[] roles)
        {
            var claims = new List<Claim>
            {
                // AuthService.cs line 148
                new Claim(ClaimTypes.NameIdentifier, userId.ToString()),
                // AuthService.cs line 149
                new Claim(ClaimTypes.Email, email)
            };

            // AuthService.cs line 150: uses ClaimTypes.Role.ToString() as the claim type
            // (NOT ClaimTypes.Role directly — the .ToString() call is in the monolith)
            foreach (var role in roles)
            {
                claims.Add(new Claim(ClaimTypes.Role.ToString(), role));
            }

            // AuthService.cs lines 152-153: token_refresh_after with binary DateTime
            DateTime tokenRefreshAfterDateTime = DateTime.UtcNow.AddMinutes(JwtTokenRefreshMinutes);
            claims.Add(new Claim("token_refresh_after", tokenRefreshAfterDateTime.ToBinary().ToString()));

            // AuthService.cs lines 155-156: HMAC-SHA256 signing
            var securityKey = new SymmetricSecurityKey(Encoding.UTF8.GetBytes(TestJwtKey));
            var credentials = new SigningCredentials(securityKey, SecurityAlgorithms.HmacSha256Signature);

            // AuthService.cs lines 157-158: token descriptor with issuer, audience, claims, expiry
            var tokenDescriptor = new JwtSecurityToken(
                issuer: TestJwtIssuer,
                audience: TestJwtAudience,
                claims: claims,
                expires: DateTime.UtcNow.AddMinutes(JwtTokenExpiryMinutes),
                signingCredentials: credentials);

            // AuthService.cs line 159: serialize to compact format
            return new JwtSecurityTokenHandler().WriteToken(tokenDescriptor);
        }

        /// <summary>
        /// Generates a valid JWT token with custom claims provided by the caller.
        /// Uses the same signing key, issuer, audience, algorithm, and expiry as
        /// <see cref="GenerateJwtToken(Guid, string, string[])"/> but allows tests
        /// to specify arbitrary claims for edge case testing (e.g., missing claims,
        /// extra claims, invalid claim types).
        /// </summary>
        /// <param name="claims">Custom claims to include in the token.</param>
        /// <returns>A JWT token string in compact serialization format.</returns>
        public string GenerateJwtToken(IEnumerable<Claim> claims)
        {
            var securityKey = new SymmetricSecurityKey(Encoding.UTF8.GetBytes(TestJwtKey));
            var credentials = new SigningCredentials(securityKey, SecurityAlgorithms.HmacSha256Signature);

            var tokenDescriptor = new JwtSecurityToken(
                issuer: TestJwtIssuer,
                audience: TestJwtAudience,
                claims: claims,
                expires: DateTime.UtcNow.AddMinutes(JwtTokenExpiryMinutes),
                signingCredentials: credentials);

            return new JwtSecurityTokenHandler().WriteToken(tokenDescriptor);
        }

        /// <summary>
        /// Generates an EXPIRED JWT token for testing JWT validation rejection.
        /// The token has an expiry of 30 minutes in the past, which should cause
        /// the JWT Bearer middleware to reject it with 401 Unauthorized
        /// (Startup.cs line 108: <c>ValidateLifetime = true</c>).
        /// </summary>
        /// <param name="userId">User ID for the NameIdentifier claim.</param>
        /// <param name="email">Email address for the Email claim.</param>
        /// <returns>An expired JWT token string in compact serialization format.</returns>
        public string GenerateExpiredJwtToken(Guid userId, string email)
        {
            var claims = new List<Claim>
            {
                new Claim(ClaimTypes.NameIdentifier, userId.ToString()),
                new Claim(ClaimTypes.Email, email)
            };

            var securityKey = new SymmetricSecurityKey(Encoding.UTF8.GetBytes(TestJwtKey));
            var credentials = new SigningCredentials(securityKey, SecurityAlgorithms.HmacSha256Signature);

            var tokenDescriptor = new JwtSecurityToken(
                issuer: TestJwtIssuer,
                audience: TestJwtAudience,
                claims: claims,
                expires: DateTime.UtcNow.AddMinutes(-30), // Expired 30 minutes ago
                signingCredentials: credentials);

            return new JwtSecurityTokenHandler().WriteToken(tokenDescriptor);
        }
