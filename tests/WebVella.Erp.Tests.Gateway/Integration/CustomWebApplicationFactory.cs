        /// <summary>
        /// Creates an <see cref="HttpClient"/> with a valid JWT Bearer token in the
        /// Authorization header, configured with <c>AllowAutoRedirect = false</c>
        /// so tests can inspect raw redirect responses (e.g., 302 to /login).
        /// </summary>
        /// <param name="userId">
        /// User ID for the <see cref="ClaimTypes.NameIdentifier"/> claim.
        /// Matches AuthService.cs line 148: <c>new Claim(ClaimTypes.NameIdentifier, user.Id.ToString())</c>
        /// </param>
        /// <param name="email">
        /// Email address for the <see cref="ClaimTypes.Email"/> claim.
        /// Matches AuthService.cs line 149: <c>new Claim(ClaimTypes.Email, user.Email)</c>
        /// </param>
        /// <param name="roles">
        /// Role names for <c>ClaimTypes.Role.ToString()</c> claims.
        /// Matches AuthService.cs line 150: <c>new Claim(ClaimTypes.Role.ToString(), role.Name)</c>
        /// </param>
        /// <returns>An authenticated <see cref="HttpClient"/> with JWT Bearer token.</returns>
        public HttpClient CreateAuthenticatedClient(Guid userId, string email, string[] roles)
        {
            var client = CreateClient(new WebApplicationFactoryClientOptions
            {
                AllowAutoRedirect = false
            });

            var token = GenerateJwtToken(userId, email, roles);
            client.DefaultRequestHeaders.Authorization =
                new AuthenticationHeaderValue("Bearer", token);

            return client;
        }

        /// <summary>
