        /// <summary>
        /// Verifies that GetNodeAuxData with a valid appId returns the full auxiliary data
        /// structure including all_entities (sorted SelectOptions with "not attached" sentinel
        /// at index 0), node_types (sorted SelectOptions from SitemapNodeType enum),
        /// app_pages (application pages with no node assignment), and all_entity_pages
        /// (pages with non-null EntityId matching the given appId).
        /// </summary>
        [Fact]
        public async Task GetNodeAuxData_ValidAppId_ReturnsEntityOptionsAndNodeTypes()
        {
            // Arrange
            var entityId1 = Guid.NewGuid();
            var entityId2 = Guid.NewGuid();
            var pageId1 = Guid.NewGuid();
            var pageId2 = Guid.NewGuid();
            var pageId3 = Guid.NewGuid();

            // Mock ReadEntities — return two entities
            // NOTE: The controller uses entity.Name (NOT entity.Label) for SelectOption.Label
            var entityListResponse = new EntityListResponse
            {
                Success = true,
                Object = new List<Entity>
                {
                    new Entity { Id = entityId1, Name = "account", Label = "Account" },
                    new Entity { Id = entityId2, Name = "contact", Label = "Contact" }
                }
            };
            _mockEntityManager
                .Setup(x => x.ReadEntities())
                .Returns(entityListResponse);

            // Mock GetAppControlledPages — pages with AppId matching; used for app_pages
            var controlledPages = new List<ErpPage>
            {
                new ErpPage
                {
                    Id = pageId1,
                    Name = "Dashboard",
                    Type = PageType.Application,
                    AppId = TestAppId,
                    NodeId = null // unattached → should appear in app_pages
                },
                new ErpPage
                {
                    Id = pageId2,
                    Name = "AccountList",
                    Type = PageType.RecordList,
                    AppId = TestAppId,
                    NodeId = TestNodeId // attached → should NOT appear in app_pages
                }
            };
            _mockPageService
                .Setup(x => x.GetAppControlledPages(It.IsAny<Guid>()))
                .Returns(controlledPages);

            // Mock GetAllPages — all pages in the system; used for all_entity_pages
            var allPages = new List<ErpPage>
            {
                new ErpPage
                {
                    Id = pageId2,
                    Name = "AccountList",
                    Type = PageType.RecordList,
                    AppId = TestAppId,
                    EntityId = entityId1,
                    NodeId = TestNodeId
                },
                new ErpPage
                {
                    Id = pageId3,
                    Name = "ContactDetails",
                    Type = PageType.RecordDetails,
                    AppId = TestAppId,
                    EntityId = entityId2,
                    NodeId = null
                },
                // Page from a different app — should NOT appear in all_entity_pages
                new ErpPage
                {
                    Id = Guid.NewGuid(),
                    Name = "OtherAppPage",
                    Type = PageType.RecordList,
                    AppId = Guid.NewGuid(),
                    EntityId = entityId1,
                    NodeId = null
                }
            };
            _mockPageService
                .Setup(x => x.GetAllPages())
                .Returns(allPages);

            var client = CreateAuthenticatedClient();

            // Act
            var response = await client.GetAsync(
                $"api/v3.0/p/sdk/sitemap/node/get-aux-info?appId={TestAppId}");

            // Assert — full structure validation
            response.StatusCode.Should().Be(HttpStatusCode.OK);
            var json = await response.Content.ReadAsStringAsync();
            var jObj = JObject.Parse(json);
            jObj["success"].Value<bool>().Should().BeTrue();

            var responseObj = jObj["object"];
            responseObj.Should().NotBeNull();

            // ── all_entities ──
            // Controller sorts entities by entity.Name, then inserts "not attached"
            // sentinel at index 0. So index 0 is always "not attached", and remaining
            // items are sorted alphabetically by entity.Name.
            var allEntities = (JArray)responseObj["all_entities"];
            allEntities.Should().NotBeNull();
            allEntities.Count.Should().Be(3); // "not attached" + 2 entities

            // Index 0: "not attached" sentinel with Value = ""
            var firstOption = allEntities[0];
            ((string)firstOption["label"]).Should().Be("not attached");
            ((string)firstOption["value"]).Should().Be("");

            // Remaining items (index 1+) should be sorted by label (entity.Name)
            var sortedEntityLabels = allEntities.Skip(1)
                .Select(e => (string)e["label"]).ToList();
            sortedEntityLabels.Should().BeInAscendingOrder();
            sortedEntityLabels.Should().Contain("account");
            sortedEntityLabels.Should().Contain("contact");

            // ── node_types ──
            // Should contain all SitemapNodeType enum values sorted by label
            var nodeTypes = (JArray)responseObj["node_types"];
            nodeTypes.Should().NotBeNull();
            nodeTypes.Count.Should().Be(3); // EntityList, ApplicationPage, Url
            var nodeTypeLabels = nodeTypes.Select(n => (string)n["label"]).ToList();
            nodeTypeLabels.Should().BeInAscendingOrder();
            // Verify known labels from SitemapNodeType enum [SelectOption] attributes
            nodeTypeLabels.Should().Contain("application page");
            nodeTypeLabels.Should().Contain("entity list");
            nodeTypeLabels.Should().Contain("url");

            // ── app_pages ──
            // Only Application-type pages with NodeId == null, ordered by page.Name
            var appPages = (JArray)responseObj["app_pages"];
            appPages.Should().NotBeNull();
            appPages.Count.Should().Be(1); // Only "Dashboard" matches
            var firstAppPage = appPages[0];
            ((string)firstAppPage["page_name"]).Should().Be("Dashboard");
            firstAppPage["page_id"].Should().NotBeNull();
            // node_id is stored as empty string "" for null NodeId (controller behavior)
            ((string)firstAppPage["node_id"]).Should().Be("");

            // ── all_entity_pages ──
            // Pages with non-null EntityId and matching AppId, sorted by page_name.
            // Controller stores type as string ((int)page.Type).ToString() and
            // node_id as string (empty string when null).
            var allEntityPages = (JArray)responseObj["all_entity_pages"];
            allEntityPages.Should().NotBeNull();
            allEntityPages.Count.Should().Be(2); // AccountList and ContactDetails (same app)
            var entityPageNames = allEntityPages
                .Select(p => (string)p["page_name"]).ToList();
            entityPageNames.Should().BeInAscendingOrder();
            entityPageNames.Should().Contain("AccountList");
            entityPageNames.Should().Contain("ContactDetails");
            // Verify structure of entity page records
            var accountListPage = allEntityPages.First(
                p => (string)p["page_name"] == "AccountList");
            accountListPage["entity_id"].Should().NotBeNull();
            accountListPage["type"].Should().NotBeNull();
            accountListPage["page_id"].Should().NotBeNull();
            // type should be string representation of RecordList int value
            ((string)accountListPage["type"]).Should().Be(((int)PageType.RecordList).ToString());
        }

