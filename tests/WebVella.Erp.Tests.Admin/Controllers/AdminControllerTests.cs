        /// <summary>
        /// Generates a signed JWT token compatible with the test server's JWT validation
        /// parameters. The token includes name, identifier, and role claims.
        /// </summary>
        /// <param name="role">The role claim value. Defaults to "administrator" for admin access.</param>
        /// <returns>A serialized JWT token string for use in Authorization: Bearer headers.</returns>
        private string GenerateJwtToken(string role = "administrator")
        {
            var securityKey = new SymmetricSecurityKey(Encoding.UTF8.GetBytes(JwtTestKey));
            var credentials = new SigningCredentials(securityKey, SecurityAlgorithms.HmacSha256);

            var claims = new[]
            {
                new Claim(ClaimTypes.Name, "testadmin"),
                new Claim(ClaimTypes.NameIdentifier, TestUserId.ToString()),
                new Claim(ClaimTypes.Role, role),
            };

            var token = new JwtSecurityToken(
                issuer: JwtIssuer,
                audience: JwtAudience,
                claims: claims,
                expires: DateTime.UtcNow.AddHours(1),
                signingCredentials: credentials);

            return new JwtSecurityTokenHandler().WriteToken(token);
        }

        /// <summary>
        /// Creates an <see cref="HttpClient"/> with a pre-set Authorization: Bearer header
        /// containing a valid JWT token for the specified role.
        /// </summary>
        /// <param name="role">The role to embed in the JWT. Defaults to "administrator".</param>
        /// <returns>An authenticated HttpClient ready to call protected endpoints.</returns>
        private HttpClient CreateAuthenticatedClient(string role = "administrator")
        {
            var client = _factory.CreateClient();
            client.DefaultRequestHeaders.Authorization =
                new AuthenticationHeaderValue("Bearer", GenerateJwtToken(role));
            return client;
        }

