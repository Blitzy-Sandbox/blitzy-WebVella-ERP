        /// <summary>
        /// Initializes the test server by creating a <see cref="WebApplicationFactory{TEntryPoint}"/>
        /// configured with mock services and a deterministic JWT signing key.
        /// </summary>
        public AdminControllerTests()
        {
            _factory = new WebApplicationFactory<Program>()
                .WithWebHostBuilder(builder =>
                {
                    // Use "Testing" environment to disable ValidateOnBuild and
                    // avoid infrastructure connectivity requirements (Redis, RabbitMQ, PostgreSQL).
                    builder.UseEnvironment("Testing");

                    // Override JWT configuration with known test values so that tokens
                    // generated by GenerateJwtToken() will pass validation.
                    builder.ConfigureAppConfiguration((context, config) =>
                    {
                        config.AddInMemoryCollection(new Dictionary<string, string>
                        {
                            ["Jwt:Key"] = JwtTestKey,
                            ["Jwt:Issuer"] = JwtIssuer,
                            ["Jwt:Audience"] = JwtAudience,
                        });
                    });

                    builder.ConfigureServices(services =>
                    {
                        // Remove all hosted services to prevent MassTransit bus startup
                        // and background job execution during tests.
                        var hostedDescriptors = services
                            .Where(d => d.ServiceType == typeof(IHostedService))
                            .ToList();
                        foreach (var descriptor in hostedDescriptors)
                        {
                            services.Remove(descriptor);
                        }

                        // Override JWT Bearer token validation parameters.
                        // Program.cs reads the JWT key from configuration at startup
                        // BEFORE ConfigureAppConfiguration overrides take effect,
                        // so we must use PostConfigure to override the already-captured
                        // TokenValidationParameters with our deterministic test key.
                        services.PostConfigure<JwtBearerOptions>(
                            JwtBearerDefaults.AuthenticationScheme,
                            options =>
                            {
                                options.TokenValidationParameters = new TokenValidationParameters
                                {
                                    ValidateIssuer = true,
                                    ValidateAudience = true,
                                    ValidateLifetime = true,
                                    ValidateIssuerSigningKey = true,
                                    ValidIssuer = JwtIssuer,
                                    ValidAudience = JwtAudience,
                                    IssuerSigningKey = new SymmetricSecurityKey(
                                        Encoding.UTF8.GetBytes(JwtTestKey)),
                                    ClockSkew = TimeSpan.FromMinutes(5)
                                };
                            });

                        // Register mock implementations for all AdminController dependencies.
                        // Using AddSingleton ensures the same mock instance is used for all
                        // scoped resolutions within the test server's DI container.
                        services.AddSingleton<IAppService>(_mockAppService.Object);
                        services.AddSingleton<IPageService>(_mockPageService.Object);
                        services.AddSingleton<IDataSourceManager>(_mockDataSourceManager.Object);
                        services.AddSingleton<IEntityManager>(_mockEntityManager.Object);
                        services.AddSingleton<IRecordManager>(_mockRecordManager.Object);
                        services.AddSingleton<ISecurityManager>(_mockSecurityManager.Object);
                        services.AddSingleton<IEntityRelationManager>(_mockEntityRelationManager.Object);
                    });
                });
        }

